package com.example.war_android.models;

public class Game {
    private String type;
    private String teamNumber;
    private String autoLower;
    private String autoOuter;
    private String autoInner;
    private String lower;
    private String outer;
    private String inner;
    private String endgame;
    private String colorWheel;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTeamNumber() {
        return teamNumber;
    }

    public void setTeamNumber(String teamNumber) {
        this.teamNumber = teamNumber;
    }

    public String getAutoLower() {
        return autoLower;
    }

    public void setAutoLower(String autoLower) {
        this.autoLower = autoLower;
    }

    public String getAutoOuter() {
        return autoOuter;
    }

    public void setAutoOuter(String autoOuter) {
        this.autoOuter = autoOuter;
    }

    public String getAutoInner() {
        return autoInner;
    }

    public void setAutoInner(String autoInner) {
        this.autoInner = autoInner;
    }

    public String getLower() {
        return lower;
    }

    public void setLower(String lower) {
        this.lower = lower;
    }

    public String getOuter() {
        return outer;
    }

    public void setOuter(String outer) {
        this.outer = outer;
    }

    public String getInner() {
        return inner;
    }

    public void setInner(String inner) {
        this.inner = inner;
    }

    public String getEndgame() {
        return endgame;
    }

    public void setEndgame(String endgame) {
        this.endgame = endgame;
    }

    public String getColorWheel() {
        return colorWheel;
    }

    public void setColorWheel(String colorWheel) {
        this.colorWheel = colorWheel;
    }

}
